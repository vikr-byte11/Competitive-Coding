void solve()
{
    ll n, m;
    cin >> n >> m;
    vi v(n + 1);
    vi pos(n + 1);
    for (int i = 1; i <= n; i++)
    {
        cin >> v[i];
        pos[v[i]] = i;
    }
    ll ans = 1;
    for (int i = 1; i < n; i++)
        if (pos[i] > pos[i + 1])
            ans++;
    ll l, r;
    set<pair<int, int>> upd;
    while (m--)
    {
        cin >> l >> r;
        if (v[l] + 1 <= n)
            upd.insert({v[l], v[l] + 1});
        if (v[l] - 1 >= 1)
            upd.insert({v[l] - 1, v[l]});
        if (v[r] - 1 >= 1)
            upd.insert({v[r] - 1, v[r]});
        if (v[r] + 1 <= n)
            upd.insert({v[r], v[r] + 1});
        for (auto x : upd)
            ans -= pos[x.first] > pos[x.second];
        swap(v[l], v[r]);
        pos[v[l]] = l;
        pos[v[r]] = r;
        for (auto x : upd)
            ans += pos[x.first] > pos[x.second];
        upd.clear();
        cout << ans << endl;
    }
}
