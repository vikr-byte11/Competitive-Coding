int dp[1001][1 << 11];
void get_mask(int cm, int row, int nem, int n, vector<int> &nm)
{
    if (row == n + 1)
    {
        nm.push_back(nem);
        return;
    }
    if ((cm & (1 << row)) != 0)
        get_mask(cm, row + 1, nem, n, nm);
    if (row != n)
        if (((cm & (1 << row)) == 0) && (cm & (1 << (row + 1))) == 0)
            get_mask(cm, row + 2, nem, n, nm);
    if ((cm & (1 << row)) == 0)
        get_mask(cm, row + 1, nem + (1 << row), n, nm);
}
int fun(int col, int mask, int m, int n)
{
    if (col == m + 1)
    {
        if (mask == 0)
            return 1;
        return 0;
    }
    if (dp[col][mask] != -1)
        return dp[col][mask];
    vector<int> nm;
    get_mask(mask, 1, 0, n, nm);
    //cout << nm.size() << endl;
    int ans = 0;
    for (auto x : nm)
        ans = (ans + fun(col + 1, x, m, n)) % md;
    return dp[col][mask] = ans;
}
void solve()
{
    memset(dp, -1, sizeof(dp));
    int n, m;
    cin >> n >> m;
    cout << fun(1, 0, m, n);
}
