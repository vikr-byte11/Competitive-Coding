ll fun(ll i, map<ll, ll> &mpp, vi &v)
{
    while (i < v.size() and mpp[v[i]] == i)
        i++;
    if (i >= v.size())
        return 0;
    ll mi = INT_MAX;
    ll ma = 0;
    ll till = mpp[v[i]];
    while (i < v.size() and till >= i)
    {
        mi = min(mi, v[i]);
        ma = max(ma, v[i]);
        till = max(till, mpp[v[i]]);
        i++;
    }
    return ma - mi + fun(i, mpp, v);
}
void solve()
{
    ll n;
    cin >> n;
    vi v(n), tmp;
    ip(v);
    tmp = v;
    srt(tmp);
    //op(tmp);
    map<ll, ll> mpp;
    for (int i = 0; i < n; i++)
        mpp[tmp[i]] = i;
    cout << fun(0, mpp, v) << endl;
}
