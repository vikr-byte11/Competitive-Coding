ll dp[5005];
struct trie
{
    bool isend;
    struct trie *child[26];
    trie()
    {
        memset(child, 0, sizeof(child));
        isend = false;
    }
};
struct trie *root;
void insert(string s)
{
    struct trie *curr = root;
    for (auto x : s)
    {
        if (!curr->child[x - 'a'])
            curr->child[x - 'a'] = new trie;
        curr = curr->child[x - 'a'];
    }
    curr->isend = true;
}
void solve()
{
    root = new trie;
    string s;
    cin >> s;
    ll k;
    cin >> k;
    for (int i = 0; i < k; i++)
    {
        string h;
        cin >> h;
        insert(h);
    }
    ll n = s.size();
    dp[n] = 1;

    for (int i = n - 1; i >= 0; i--)
    {
        //search the word
        //from i to n
        struct trie *curr = root;
        ll ans = 0;
        for (int j = i; j < n; j++)
        {
            if (curr->child[s[j] - 'a'] == 0)
                break;
            curr = curr->child[s[j] - 'a'];
            if (curr->isend)
            {
                ans += (dp[j + 1]) % md;
                ans %= md;
            }
        }
        dp[i] = ans;
    }
    cout << dp[0];
}
