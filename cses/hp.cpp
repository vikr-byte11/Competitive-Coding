void heapify(vector<ll> &v, int n, int idx)
{
    int lchild = 2 * idx + 1;
    int rchild = 2 * idx + 2;
    int largest = idx;
    if (lchild < n and v[largest] < v[lchild])
        largest = lchild;
    if (rchild < n and v[largest] < v[rchild])
        largest = rchild;
    if (largest != idx)
    {
        swap(v[largest], v[idx]);
        heapify(v, n, largest);
    }
}
void del(vi &v, int n)
{
    ll le = v[n - 1];
    swap(v[0], v[n - 1]);
    n--;
    heapify(v, n, 0);
}
void ins(vi v, int n, int ele)
{
    n = n + 1;
    v[n - 1] = ele;
    int i = n - 1;
    while (i > 0)
    {
        ll parent = (i - 1) / 2;
        if (v[parent] < v[i])
        {
            swap(v[parent], v[i]);
            i = parent;
        }
    }
}
