void solve()
{
    string s1, s2;
    cin >> s1 >> s2;
    ll a = s1.length();
    ll b = s2.length();
    int dp[a + 1][b + 1];
    memset(dp, 0, sizeof(dp));
    for (int i = 1; i <= a; i++)
    {
        for (int j = 1; j <= b; j++)
        {
            if (s1[i - 1] == s2[j - 1])
            {
                dp[i][j] = 1 + dp[i - 1][j - 1];
            }
            dp[i][j] = max(dp[i][j], max(dp[i][j - 1], dp[i - 1][j]));
        }
    }
    string ans;
    int i = a, j = b;
    while (i > 0 and j > 0)
    {
        if (s1[i - 1] == s2[j - 1])
        {
            ans.push_back(s1[i - 1]);
            i--;
            j--;
        }
        else
        {
            if (dp[i - 1][j] > dp[i][j - 1])
            {
                ans.push_back(s1[i - 1]);
                i--;
            }
            else
            {
                ans.push_back(s2[j - 1]);
                j--;
            }
        }
    }
    while (i > 0)
    {
        ans.push_back(s1[i - 1]);
        i--;
    }

    while (j > 0)
    {
        ans.push_back(s2[j - 1]);
        j--;
    }
    rev(ans);
    cout << ans;
}
