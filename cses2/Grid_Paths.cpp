ll dp[1001][1001];
ll fun(int i, int j, vector<vector<char>> &v, ll n)
{
    if (i < 0 || i >= n || j < 0 || j >= n || v[i][j] == '*')
        return 0;
    if (i == n - 1 and j == n - 1)
        return 1;
    if (dp[i][j] != -1)
        return dp[i][j];
    //her it is dot
    v[i][j] = '*';
    ll op1 = fun(i + 1, j, v, n);
    ll op2 = fun(i, j + 1, v, n);
    v[i][j] = '.';
    return dp[i][j] = (op1 + op2) % 1000000007;
}
void solve()
{
    ll n;
    cin >> n;
    memset(dp, -1, sizeof(dp));
    vector<vector<char>> v(n, vector<char>(n));
    for (int i = 0; i < n; i++)
        for (int j = 0; j < n; j++)
            cin >> v[i][j];
    cout << fun(0, 0, v, n);
}
